import pandas as pd

def analyze_genre_performance(df):
    """Analyze franchise vs standalone performance by genre"""

    if df.empty or 'primary_genre' not in df.columns:
        print(" No genre data available")
        return pd.DataFrame()

    print("\n Analyzing genre performance...")

    df_valid = df[
        (df['primary_genre'].notna()) &
        (df['roi'].notna()) &
        (df['roi'] > -100) &
        (df['roi'] < 500)
    ].copy()

    genre_stats = df_valid.groupby(['primary_genre', 'is_franchise']).agg({
        'roi': 'mean',
        'revenue': 'mean',
        'vote_average': 'mean',
        'title': 'count'
    }).reset_index()

    genre_stats.columns = ['genre', 'is_franchise', 'avg_roi', 'avg_revenue', 'avg_rating', 'count']

    genre_comparison = genre_stats.pivot_table(
        index='genre',
        columns='is_franchise',
        values=['avg_roi', 'avg_revenue', 'avg_rating']
    )

    genre_comparison.columns = [f'{val}_{col}' for val, col in genre_comparison.columns]
    genre_comparison = genre_comparison.reset_index()

    col_mapping = {}
    for col in genre_comparison.columns:
        if col == 'genre':
            continue
        elif '_False' in col:
            col_mapping[col] = col.replace('_False', '_standalone')
        elif '_True' in col:
            col_mapping[col] = col.replace('_True', '_franchise')

    genre_comparison = genre_comparison.rename(columns=col_mapping)

    if 'avg_roi_franchise' in genre_comparison.columns and 'avg_roi_standalone' in genre_comparison.columns:
        genre_comparison['roi_difference'] = (
            genre_comparison['avg_roi_franchise'] - genre_comparison['avg_roi_standalone']
        )

    if 'avg_revenue_franchise' in genre_comparison.columns and 'avg_revenue_standalone' in genre_comparison.columns:
        genre_comparison['revenue_multiplier'] = (
            genre_comparison['avg_revenue_franchise'] / genre_comparison['avg_revenue_standalone']
        )
        genre_comparison['avg_revenue_franchise'] = genre_comparison['avg_revenue_franchise'] / 1e6
        genre_comparison['avg_revenue_standalone'] = genre_comparison['avg_revenue_standalone'] / 1e6

    if 'roi_difference' in genre_comparison.columns:
        genre_comparison = genre_comparison.sort_values('roi_difference', ascending=False)

    print(f" Analyzed {len(genre_comparison)} genres")
    return genre_comparison


def analyze_marketability_impact(df):
    """Analyze cast marketability (Q Score proxy) impact"""

    if df.empty or 'cast_marketability' not in df.columns:
        print(" No marketability data available")
        return pd.DataFrame()

    print("\n Analyzing cast marketability impact...")

    df_valid = df[
        (df['cast_marketability'].notna()) &
        (df['roi'].notna()) &
        (df['roi'] > -100) &
        (df['roi'] < 500)
    ].copy()

    market_stats = df_valid.groupby('cast_marketability').agg({
        'revenue': 'mean',
        'roi': 'mean',
        'vote_average': 'mean',
        'title': 'count'
    }).reset_index()

    market_stats.columns = ['q_score_range', 'avg_revenue', 'avg_roi', 'avg_rating', 'sample_size']

    success_rates = df_valid.groupby('cast_marketability').apply(
        lambda x: (x['roi'] > 100).sum() / len(x) * 100 if len(x) > 0 else 0
    ).reset_index()
    success_rates.columns = ['q_score_range', 'success_rate']

    market_analysis = market_stats.merge(success_rates, on='q_score_range')
    market_analysis['avg_revenue'] = market_analysis['avg_revenue'] / 1e6

    order = ['Low (Q<25)', 'Medium (Q 25-40)', 'High (Q>40)']
    market_analysis['q_score_range'] = pd.Categorical(
        market_analysis['q_score_range'],
        categories=order,
        ordered=True
    )
    market_analysis = market_analysis.sort_values('q_score_range')

    print(f" Analyzed {len(market_analysis)} marketability tiers")
    return market_analysis


