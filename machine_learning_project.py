def process_movie_data(df):
    """Process and clean movie data for analysis"""

    if df.empty:
        return df

    print("\nProcessing movie data...")

    # Create working copy
    df_proc = df.copy()

    # Convert numeric columns
    numeric_cols = ['budget', 'revenue', 'vote_average', 'vote_count', 'popularity']
    for col in numeric_cols:
        if col in df_proc.columns:
            df_proc[col] = pd.to_numeric(df_proc[col], errors='coerce')

    # Calculate ROI
    if 'budget' in df_proc.columns and 'revenue' in df_proc.columns:
        # Ensure budget > 0 to avoid division by zero
        df_proc = df_proc[df_proc['budget'] > 0].copy() # Added .copy() to avoid SettingWithCopyWarning
        df_proc['roi'] = ((df_proc['revenue'] - df_proc['budget']) / df_proc['budget'] * 100).replace([np.inf, -np.inf], np.nan)

    # Extract year from release_date
    if 'release_date' in df_proc.columns:
        df_proc['release_year'] = pd.to_datetime(df_proc['release_date'], errors='coerce').dt.year

    # Process genres (JSON format)
    if 'genres' in df_proc.columns:
        df_proc['genre_list'] = df_proc['genres'].apply(extract_genres)
        # Ensure 'primary_genre' column is created even if some genre_list are empty
        df_proc['primary_genre'] = df_proc['genre_list'].apply(lambda x: x[0] if x else None)

    # Identify franchise/sequel films
    if 'title' in df_proc.columns:
        df_proc['is_sequel'] = df_proc['title'].str.contains(
            r'(\d|II|III|IV|Part|Chapter|Episode)',
            case=False,
            na=False
        )

    if 'belongs_to_collection' in df_proc.columns:
        df_proc['is_franchise'] = df_proc['belongs_to_collection'].notna()
    elif 'is_sequel' in df_proc.columns:
        # Fallback to 'is_sequel' if 'belongs_to_collection' is not present
        df_proc['is_franchise'] = df_proc['is_sequel']
    else:
        df_proc['is_franchise'] = False # Default to false if neither is available

    # Budget categories
    if 'budget' in df_proc.columns and not df_proc['budget'].empty:
        df_proc['budget_category'] = pd.cut(
            df_proc['budget'] / 1e6,
            bins=[0, 50, 100, 150, 200, np.inf],
            labels=['< $50M', '$50M-$100M', '$100M-$150M', '$150M-$200M', '> $200M']
        )

    # Cast popularity score (proxy for Q Score using popularity metric)
    if 'popularity' in df_proc.columns and not df_proc['popularity'].empty:
        df_proc['cast_marketability'] = pd.cut(
            df_proc['popularity'],
            bins=[0, 25, 40, np.inf],
            labels=['Low (Q<25)', 'Medium (Q 25-40)', 'High (Q>40)']
        )

    print(f" Processed {len(df_proc):,} records")
    return df_proc

def extract_genres(genre_str):
    """Extract genre names from JSON string"""
    try:
        if pd.isna(genre_str):
            return []
        genres = json.loads(genre_str.replace("'", '"'))
        return [g.get('name', '') for g in genres if isinstance(g, dict)]
    except:
        return []

